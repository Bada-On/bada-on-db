USE `krampoline`;

DROP TABLE IF EXISTS `marker`;
DROP TABLE IF EXISTS `day_weather`;
DROP TABLE IF EXISTS `short_weather`;
DROP TABLE IF EXISTS `region`;

CREATE TABLE marker
(
    id        BIGINT AUTO_INCREMENT PRIMARY KEY,
    activity  enum ('DIVING','PHOTO_SHOOTING','SCUBA_DIVING','SNORKELING','SUNRISE_SUNSET_VIEWING','SURFING','SWIMMING'),
    latitude  FLOAT(53) NOT NULL,
    longitude FLOAT(53) NOT NULL,
    region_id BIGINT,
    name      VARCHAR(255)
);

CREATE TABLE region
(
    id   BIGINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255)
);

CREATE TABLE day_weather
(
    day_weather_id BIGINT AUTO_INCREMENT PRIMARY KEY,
    high_tide_1    TIME(6),
    high_tide_2    TIME(6),
    low_tide_1     TIME(6),
    low_tide_2     TIME(6),
    sun_rise_time  TIME(6),
    sun_set_time   TIME(6),
    weather_date   DATE,
    region_id      BIGINT
);

CREATE TABLE short_weather
(
    short_weather_id BIGINT AUTO_INCREMENT PRIMARY KEY,
    cloud            INTEGER,
    temp             FLOAT(53),
    tidal            INTEGER,
    visible_distance INTEGER,
    wave_height      INTEGER,
    weather_date     DATE,
    weather_time     TIME(6),
    region_id        BIGINT
);

ALTER TABLE marker
    ADD CONSTRAINT FK_marker_region
        FOREIGN KEY (region_id)
            REFERENCES region (id);

ALTER TABLE day_weather
    ADD CONSTRAINT FK_day_weather_region
        FOREIGN KEY (region_id)
            REFERENCES region (id);

ALTER TABLE short_weather
    ADD CONSTRAINT FK_short_weather_region
        FOREIGN KEY (region_id)
            REFERENCES region (id);


